from odoo import models, fields, api
from odoo.tools.translate import _
from logging import getLogger


_logger = getLogger(__name__)

 
class CivilServiceTrackerVacancyType(models.Model):

    _name = 'civil.service.tracker.vacancy.type'
    _description = u'Civil service tracker vacancy type'

    _table = 'cst_vacancy_type'

    _rec_name = 'name'
    _order = 'name ASC'

    name = fields.Char(
        string='Name',
        required=True,
        readonly=False,
        index=True,
        default=None,
        help='Category of vacancy (e.g. General, Disabled)',
        translate=True
    )

    description = fields.Text(
        string='Description',
        required=False,
        readonly=False,
        index=False,
        default=None,
        help='Optional description providing additional context',
        translate=True
    )

    active = fields.Boolean(
        string='Active',
        required=False,
        readonly=False,
        index=False,
        default=True,
        help='Indicates whether this administrative scope is currently active'
    )
